preco = 10
print(preco)

# Convertendo inteiro para ponto flutuante
preco = float(preco)
print(preco)

# Convertendo de flutuante pra inteiro
preco = 10.3
preco = int(preco)
print(preco)

# Convertendo numero para string
preco = 10.50
idade = 28

print(str(preco))

print(str(idade))

texto = f"idade {idade} preco {preco}"
print(texto)